let x = 10;
console.log('Value of x is ' + x);

x = x + 1;
console.log('Value of x after x = x + 1 is ' + x);

x++;
console.log('Value of x after x++ is ' + x);

x += 1;
console.log('Value of x after x += 1 is ' + x);

x += 5;
console.log('Value of x after x += 5 is ' + x);


x *= 2;
console.log('Value of x after x *= 2 is ' + x);