//Creating an array
let selectedColors = ['red', 'blue'];
console.log(selectedColors);

//adding an item to array
selectedColors[2] = 'green';
console.log(selectedColors);

//adding different types of items to an array
selectedColors[3] = '100';
console.log(selectedColors);


//type of array is an object.
//We can access it with dot notation
console.log(selectedColors.length);
//length property shows the number of items in the array.